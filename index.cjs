const fs = require("fs");

function ipToBinary(ip) {
  return ip
    .split(".")
    .map((octet) => parseInt(octet, 10).toString(2).padStart(8, "0"))
    .join("");
}

function binaryToIp(binary) {
  return binary
    .match(/.{8}/g)
    .map((byte) => parseInt(byte, 2))
    .join(".");
}

function getNetworkAddress(ip, mask) {
  const ipBinary = ipToBinary(ip);
  const networkBinary = ipBinary.slice(0, mask) + "0".repeat(32 - mask);
  return binaryToIp(networkBinary);
}

function getCIDRFromRange(startIp, endIp) {
  const startBinary = ipToBinary(startIp);
  const endBinary = ipToBinary(endIp);
  let i = 0;
  while (i < 32 && startBinary[i] === endBinary[i]) {
    i++;
  }
  return `${getNetworkAddress(startIp, i)}/${i}`;
}

function convertIPRangesToCIDR(ipRanges) {
  return ipRanges.map(([startIp, endIp, _]) =>
    getCIDRFromRange(startIp, endIp)
  );
}

